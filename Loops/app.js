console.log("Odd Numbers");

for (let b=1; b<=15; b=b+2){
    console.log(b);
}

console.log("Even Numbers");

for (let b=2; b<=10; b=b+2){
    console.log(b);
}

console.log("Reverse Even Numbers");

for (let b=10; b>=2; b=b-2){
    console.log(b);
}